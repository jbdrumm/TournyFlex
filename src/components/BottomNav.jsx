export default function BottomNav() {
  const navigate = useNavigate()
  const location = useLocation()
  const { isCommissioner } = useAuth()
  const path = location.pathname

  const items = [
    { path: '/',            icon: <HomeIcon />,    label: 'Home'    },
    { path: '/leaderboard', icon: <TrophyIcon />,  label: 'Board'   },
    { path: '/scorecard',   icon: <ClubIcon />,    label: 'Score'   },
    { path: '/groups',      icon: <GroupIcon />,   label: 'Groups'  },
    { path: '/history',     icon: <HistoryIcon />, label: 'History' },
  ]

  return (
    <nav className="bottom-nav">
      {items.map(item => (
        <button key={item.path}
          className={`nav-item${path === item.path ? ' active' : ''}`}
          onClick={() => navigate(item.path)}>
          {item.icon}<span>{item.label}</span>
        </button>
      ))}
      {/* Admin tab: only visible to commissioner */}
      {isCommissioner && (
        <button
          className={`nav-item${path === '/commissioner' || path === '/login' ? ' active' : ''}`}
          onClick={() => navigate('/commissioner')}>
          <SettingsIcon /><span>Admin</span>
        </button>
      )}
    </nav>
  )
}
